def input_loop(x0, y0):
    path = ""
    x = x0
    y = y0

    while True:
        if x < 100:
            y = y + 1
            x = x + 1
            path += "A"
        elif y > 0:
            y = y - 1
            path += "B"
        else:
            break
    return [x, y, path]


